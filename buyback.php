    <script>
        // Script หลักของมึง (ตัวอย่าง)
        document.addEventListener('DOMContentLoaded', function() {
            // AOS Initialization
            AOS.init({
                duration: 1000, // Animation duration
                once: true, // Whether animation should happen only once - while scrolling down
            });

            // SplideJS Initialization (ถ้ามี gallery)
            if (document.querySelector('.splide')) {
                new Splide('.splide', {
                    type: 'loop', // หรือ 'slide', 'fade'
                    perPage: 3, // จำนวนสไลด์ที่แสดงต่อหน้า
                    gap: '1rem',
                    autoplay: true,
                    pauseOnHover: true,
                    breakpoints: {
                        768: {
                            perPage: 2,
                        },
                        576: {
                            perPage: 1,
                        }
                    }
                }).mount();
            }

            // Counter Animation (ถ้ามี)
            const counters = document.querySelectorAll('.counter');
            const speed = 200; // The lower the slower

            counters.forEach(counter => {
                const animateCount = () => {
                    const target = +counter.getAttribute('data-count');
                    const count = +counter.innerText;
                    const inc = Math.ceil(target / speed);

                    if (count < target) {
                        counter.innerText = count + inc;
                        setTimeout(animateCount, 1);
                    } else {
                        counter.innerText = target.toLocaleString(); // ใส่ comma ให้ตัวเลข
                    }
                };
                //หน่วงเวลาก่อนเริ่มนับ หรือจะใช้ Intersection Observer ก็ได้
                setTimeout(animateCount, 500);
            });


        });
